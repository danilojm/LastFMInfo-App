package com.danilomendes.Model;

/**
 * Represents biography information.
 */
public class Bio {
    /** The summary of the biography */
    private String summary;

    /**
     * Get the summary of the biography.
     * @return The summary
     */
    public String getSummary() {
        return summary;
    }

    /**
     * Set the summary of the biography.
     * @param summary The summary to set
     */
    public void setSummary(String summary) {
        this.summary = summary;
    }
}
